export function getFeaturesByCategory(categoryId: string): Feature[] {
  return allFeatures.filter((f) => f.category === categoryId);
}

export function getFeaturesByTier(tier: PlanTier): Feature[] {
  const tierOrder = ["free", "starter", "pro", "enterprise"];
  const tierIndex = tierOrder.indexOf(tier);
  return allFeatures.filter((f) => tierOrder.indexOf(f.tier) <= tierIndex);
}

export function getPopularFeatures(): Feature[] {
  return allFeatures.filter((f) => f.popular);
}

export function getNewFeatures(): Feature[] {
  return allFeatures.filter((f) => f.new);
}
